public  class twoDarray{
    public static  void main(String[] args){
        int nums[][]=new int[3][4];

        //print the 2D array
        for(int i=0;i<3;i++){
            for(int j=0;j<4;j++){
                System.out.print(nums[i][j]+" ");
            }
            System.out.println();
        }


    }
}